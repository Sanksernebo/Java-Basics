package week2;

public class Ex29 {
    public static void main(String[] args) {
        // Loop from 2 to 100
        for (int i = 2; i <= 100; i++) {
            // Check if the current number is even
            if (i % 2 == 0) {
                System.out.println(i); // Print the even number
            }
        }
    }
}
